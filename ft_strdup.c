#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t				i;
	char				*dest;
	size_t				ptrs1;

	i = 0;
	ptrs1 = ft_strlen(s1);
	dest = malloc(ptrs1 + 1);
	if (dest == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
